#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	while (str[i] != c)
	{
		if (i == 0)
			return (NULL);
		i--;
	}
	return ((char*)&str[i]);
}
